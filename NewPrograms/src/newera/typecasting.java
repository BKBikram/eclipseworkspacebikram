package newera;

public class typecasting {

	public static void main(String[] args) {
			int i = 65;
			long l = i;
			float f = i;
			Character ch=(char) i;
			System.out.println("Int value "+i);
			System.out.println("Long value "+l);
			System.out.println("Float value "+f);
			System.out.println("character value:"+ch);
			}

	}
